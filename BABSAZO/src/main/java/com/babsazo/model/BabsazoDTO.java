package com.babsazo.model;

public class BabsazoDTO {

	private String babnm;

	private String nickname;

	private String babrole;

	private String comments;

	private String img;

	public String getBabnm() {
		return babnm;
	}

	public void setBabnm(String babnm) {
		this.babnm = babnm;
	}

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public String getBabrole() {
		return babrole;
	}

	public void setBabrole(String babrole) {
		this.babrole = babrole;
	}

	public String getComments() {
		return comments;
	}

	public void setComments(String comments) {
		this.comments = comments;
	}

	public String getImg() {
		return img;
	}

	public void setImg(String img) {
		this.img = img;
	}

}
